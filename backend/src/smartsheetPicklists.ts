/**
 * Valida se um valor está na lista PICKLIST correspondente
 * @param nomeColuna Nome da coluna (case-insensitive)
 * @param valor Valor a ser validado
 * @returns true se o valor é válido, false caso contrário
 */
export function validarPicklist(nomeColuna: string, valor: string): boolean {
  const lista = PICKLIST_MAP[nomeColuna] || 
                Object.entries(PICKLIST_MAP).find(([key]) => 
                  key.toLowerCase() === nomeColuna.toLowerCase()
                )?.[1];
  
  if (!lista) {
    return true; // Se não há lista definida, aceita qualquer valor
  }
  
  return lista.some(opcao => 
    opcao.toLowerCase().trim() === valor.toLowerCase().trim()
  );
}

